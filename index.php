                <h3>Morte</h3>
                <p>
                    Lee morreu em 12 de novembro de 2018, aos 95 anos, no Cedars-Sinai Medical Center em Los Angeles, 
                    Califórnia. Foi levado para o hospital em uma ambulância de emergência médica no início do dia e 
                    morreu pouco depois. Em 2017 havia sofrido um surto de pneumonia e passou a ter problemas de 
                    visão. Morava com sua filha Joan Celia desde a morte da esposa Joan Lee, em 2017. Joan Lee morreu 
                    aos 93 anos, seis meses antes do casal completar setenta anos de união.
                </p>
                <p>
                    Em 27 de novembro de 2018, 15 dias após a morte, o site TMZ teve acesso a certidão de óbito de Lee e a 
                    causa da morte dele foi revelada: ele teve paradas cardíaca e respiratória. Além disso, o certificado 
                    mostrou que ele sofreu de pneumonia por aspiração, uma complicação decorrente de uma grande quantidade 
                    de comida, ácido estomacal ou saliva nos pulmões.
                </p>
