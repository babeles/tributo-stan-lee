                <div class="subtitulo">Stanley Martin Lieber</div>
                <div class="info-nasc-mort">Nova Iorque, 28 de dezembro de 1922 — Los Angeles, 12 de novembro de 2018</div>
            </header>

            <article class="artigo">
                <hr>
                <p>
                    <strong>Stanley Martin Lieber</strong>, mais conhecido como <strong>Stan Lee</strong> (Nova Iorque, 28 de dezembro de 1922
                    — Los Angeles, 12 de novembro de 2018), foi um escritor, editor, publicitário, produtor, diretor, 
                    empresário e ator norte-americano. Foi editor-chefe e presidente da Marvel Comics antes de deixar a 
                    empresa para se tornar presidente emérito da editora, bem como um membro do conselho editorial. 
                    Lee também era conhecido por fazer várias aparições em filmes da Universo Cinematográfico Marvel.
                </p>
                <p>
                    Em colaboração com vários artistas, incluindo Jack Kirby e Steve Ditko, concriou diversos super-heróis 
                    incluindo o Homem-Aranha, Hulk, Doutor Estranho, Quarteto Fantástico, Demolidor, Pantera Negra e os 
                    X-Men. Além disso, desafiou a organização de censura da indústria de quadrinhos americana, o Comics 
                    Code Authority, indiretamente levando-a a atualizar suas políticas. Lee liderou a expansão da Marvel 
                    Comics de uma pequena divisão de uma editora para uma grande corporação de multimídia.
                </p>
                <p>
                    Stan Lee foi introduzido no Will Eisner Award Hall of Fame em 1994, no Jack Kirby Hall of Fame em 1995 
                    e recebeu uma National Medal of Arts em 2008.
                </p>
            
                <hr>
                <h3>Origem</h3>
                <p>
                    Stanley Martin Lieber nasceu em Manhattan, Nova Iorque, Estados Unidos, no dia 28 de dezembro de 1922, 
                    filho do casal Jack e Celia Lieber, ambos judeus imigrantes da Romênia. Seu pai, um alfaiate, e sua mãe,
                    dona-de-casa, tiveram ainda mais um filho, Larry, nascido em 1931 (que assim como o irmão mais velho fez 
                    carreira no mundo dos quadrinhos). A família de Lee era relativamente pobre, tendo ele morado boa parte 
                    da infância e da adolescência em um "quarto-e-sala" na região do Bronx, na periferia de Nova Iorque, em 
                    que ele e o irmão dividiam o diminuto e único quarto do apartamento e os pais dormiam em um sofá-cama na 
                    sala. Durante a adolescência, Lee estudou na DeWitt Clinton High School, também localizado no Bronx.
                </p>
                <p>
                    Desde pequeno Lee gostava de escrever, e seu sonho durante a adolescência era escrever um grande 
                    romance um dia. Inteligente, formou-se relativamente cedo, aos quinze anos, tendo trabalhado 
                    escrevendo obituários em jornais, entregando sanduíches para escritórios no Rockefeller Center, 
                    trabalhando como office boy em uma fábrica manufatureira e como "lanterninha" do Teatro Rivoli na 
                    Broadway.
                </p>
                
                <hr>
                <h3>Início de carreira</h3>
                <p>
                    Com a ajuda de seu tio Robbie Solomon, Lee tornou-se assistente em 1939 na editora Timely Comics, 
                    divisão de revistas pulps e revistas em quadrinhos de Martin Goodman, que na década de 1960, 
                    evoluiria para a Marvel Comics. Lee, cuja prima Jean, era a esposa de Goodman, foi formalmente 
                    contratado pelo editor da Timely, Joe Simon.
                </p>
                <div class="img-artigo">
                    <div>
                        <p>
                            Seu primeiro trabalho publicado foi um conto ilustrado por Jack Kirby intitulado Captain America 
                            Foils the Traitor's Revenge, publicado em Captain America Comics (de maio de 1941), usando o pseudônimo
                            Stan Lee. Explicou mais tarde em sua autobiografia e várias outras fontes, que pretendia salvar 
                            seu nome dado para mais trabalho literário. Esta história inicial também introduziu o uso do escudo 
                            do Capitão América como uma arma de arremesso. Iniciou criando histórias em quadrinhos para as 
                            revistas Headline Hunter e Foreign Correspondent, publicada dois números depois. A primeira cocriação 
                            de super-heróis de Lee foi o Destroyer, em Mystic Comics (agosto de 1941). Outros personagens que 
                            cocriou durante este período, os fãs e historiadores chamam de Era de ouro das histórias em quadrinhos 
                            americanas incluem Jack Frost, estreando em USA Comics (agosto de 1941), e Father Time, estreando em 
                            Captain America Comics #6 (agosto de 1941).
                        </p>
                        <p>
                        Quando Joe Simon e seu parceiro criativo, Jack Kirby, saíram no final de 1941, após uma discussão com 
                        Goodman, o editor de trinta anos nomeou Lee, com menos de 19 anos, como editor interino. um talento 
                        para o negócio que o levou a permanecer como editor-chefe da divisão de revistas em quadrinhos, bem 
                        como diretor de arte durante boa parte desse tempo, até 1972, quando sucederia Goodman como publisher.
                        </p>
                        
                    </div>
                    <figure>
                        <img src="assets/img/220px-CapAward.png" alt="Escudo do Capitão América"/>
                        <figcaption>
                            O primeiro texto publicado por Stan Lee na Timely foi uma história em que mostrou o 
                                escudo do Capitão América como uma arma de arremesso.
                        </figcaption>
                    </figure>
                </div>
                <div class="img-artigo">
                    <div>
                        <p>
                            Lee ingressou no Exército no início de 1942 e serviu como membro do Signal Corps, fazendo manutenção 
                            em postes telegráficos e outros equipamentos de comunicação. Mais tarde, foi transferido para a Divisão 
                            de Filmes de Treinamento, onde trabalhou escrevendo manuais, filmes de treinamento, slogans e, ocasionalmente, 
                            fazendo cartoons. Sua classificação militar, afirmou, era dramaturgo; acrescenta que apenas nove homens 
                            no exército dos Estados Unidos receberam esse título. Vincent Fago, editor da seção "animation comics" do Timely, 
                            que publica quadrinhos de animais engraçados, preenchida até que Lee retornou de seu serviço militar na Segunda 
                            Guerra Mundial em 1945
                        </p>
                    </div>
                    <figure>
                        <img src="assets/img/220px-Stan_Lee_in_the_army.jpg" alt="Escudo do Capitão América"/>
                        <figcaption>
                            Stan Lee no Exército
                        </figcaption>
                    </figure>
                </div>
                <p>
                    Em meados da década de 1950, quando a empresa passou a ser conhecida como Atlas Comics, 
                    escreveu histórias em vários gêneros, incluindo romance, faroeste, humor, ficção científica, 
                    aventura medieval, horror e suspense. Na década de 1950, uniu-se ao seu colega de quadrinhos 
                    Dan DeCarlo para produzir a tira de jornal, My Friend Irma, baseada na comédia de rádio 
                    estrelada por Marie Wilson. No final da década, Lee ficou insatisfeito com sua carreira e 
                    pensou em abandonar o ramo
                </p>
                <p>
                    Naquela época, em uma campanha moralizadora realizada pelo psiquiatra Fredric Wertham e pelo senador 
                    Estes Kefauver, estes acusaram as revistas em quadrinhos de corromper as mentes dos jovens leitores 
                    com imagens de violência e sexualidade ambíguas. Em 1954, Wertham publicou o livro Sedução do 
                    Inocente, sobre suas teorias. Isso fez com que o Subcomitê sobre Delinquência Juvenil do Senado 
                    dos EUA começasse a investigar os efeitos da mídia de massa publicada pela indústria dos quadrinhos. 
                    Em abril e junho daquele ano, os senadores organizaram audiências públicas em Nova York. Entre os 
                    participantes, estava William Gaines, editor da EC Comics. O senador Kefauver mostrou a ele uma capa 
                    do ilustrador Johnny Craig, da EC Comics com uma cabeça decepada e um machado ensanguentado, da 
                    revista Crime SuspenStories, dizendo que aquilo era de "bom gosto" para uma revista de terror e não 
                    para leitura infanto-juvenil. O assunto levou a uma reportagem de primeira página no The New York
                    Times, com o título No Harm in Horror (Horror inofensivo).
                </p>
                <p>
                    Aquelas audiências marcaram o começo do fim da EC Comics. Gaines tentou se associar a outros editores 
                    para se defender, mas os outros estavam procurando uma solução rápida para o problema, que já ameaçava
                    seus negócios. Em outubro de 1954, os editores formaram a Association of Comics Magazine Publishers 
                    (Associação dos Editores de Revistas em Quadrinhos da América), adotando um código regulador, o 
                    Comics Code Authority. Gaines inicialmente se recusou a adotar o CCA e submeter seus quadrinhos para 
                    revisão. A maioria dos distribuidores, no entanto, apenas adquiriam quadrinhos aprovados com o código 
                    e Gaines se viu obrigado a adotar o controle. Gaines deixou a associação em outubro de 1955, por 
                    discordar do rigor da censura imposta para obter o CCA nas suas publicações. Depois disso, e com a 
                    publicação da revista Mad, seu faturamento dobrou.
                </p>
                
                <hr>
                <h3>Revolução da Marvel</h3>
                <p>
                    No final dos anos 50, o editor da DC Comics, Julius Schwartz, reviveu o arquétipo do super-herói e 
                    experimentou um sucesso significativo com sua versão atualizada do Flash, e mais tarde com a super 
                    equipe da Liga da Justiça da América. Em resposta, o editor Martin Goodman designou Lee para criar 
                    uma nova equipe de super-heróis. A esposa de Lee sugeriu que ele experimentasse histórias que 
                    preferia, já que ele planejava mudar de carreira e não tinha nada a perder.
                </p>
                <p>
                    Lee aceitou o conselho da esposa, dando aos seus super-heróis uma humanidade defeituosa, uma mudança 
                    dos arquétipos ideais que eram tipicamente escritos para pré-adolescentes. Antes disso, a maioria dos 
                    super-heróis era idealmente perfeita, sem problemas sérios e duradouros. Introduziu personagens 
                    complexos e naturalistas que poderiam ter maus ânimos, ataques de melancolia e vaidade; eles brigavam 
                    entre si, preocupados em pagar suas contas e impressionar namoradas, ficavam entediados e às vezes 
                    ficavam doentes. Seus super-heróis capturaram a imaginação dos adolescentes e jovens adultos, e as 
                    vendas aumentaram drasticamente.
                </p>
                <p>
                    Os primeiros super-heróis que Lee e o desenhista Jack Kirby criaram juntos foram o Quarteto Fantástico,
                    baseado em uma equipe de super-heróis de Kirby, Challengers of the Unknown, publicada pela DC Comics. 
                    A popularidade imediata da equipe levou Lee e os ilustradores da Marvel a produzirem uma série de 
                    novos títulos. Mais uma vez trabalhando com Kirby, cocriou Hulk, Thor, Homem de Ferro, e X-Men; com 
                    Bill Everett, Demolidor; e com Steve Ditko, Doutor Estranho, e o personagem mais bem-sucedido da Marvel,
                    o Homem-Aranha. Todos eles viviam em um universo completamente compartilhado. Lee e Kirby reuniram vários
                    de seus personagens recém-criados no título Avengers e reviveram personagens dos anos 40, como o Namor e 
                    o Capitão América
                </p>
                <p>
                    A revolução de Lee se estendeu além dos personagens e enredos até a maneira como os quadrinhos 
                    engajaram os leitores e construíram um senso de comunidade entre fãs e criadores. Ele introduziu a 
                    prática de incluir regularmente um painel de crédito na página inicial de cada história, nomeando 
                    não apenas o roteirista e o desenhista, mas também o arte-finalista e o letrista. Notícias regulares 
                    sobre os membros da equipe da Marvel e as próximas histórias foram apresentadas na página Bullpen 
                    Bulletins que (como as sessões de cartas que apareceram em cada título) foi escrita em um estilo 
                    amigável e tagarela. Lee comentou que seu objetivo era que os fãs pensassem nos criadores de 
                    quadrinhos como amigos, e considerou uma marca de seu sucesso nessa frente que, em um momento em que 
                    as cartas para outros editores de quadrinhos eram tipicamente endereçadas a "Querido Editor", cartas
                    para a Marvel vinha endereçadas aos autores (Exemplo: "Queridos Stan e Jack"). Por volta de 1967, a 
                    marca já estava bastante satisfeita na cultura popular que um programa de rádio da WBAI em 3 de março 
                    com Lee e Kirby como convidados foi intitulado "Will Success Spoil Spiderman" 
                </p>
                <p>
                    Ao longo da década de 1960, Lee roteirizou, dirigiu e editou a maioria das séries da Marvel, moderou 
                    as sessões de cartas, escreveu uma coluna mensal chamada "Stan's Soapbox" e escreveu uma cópia 
                    promocional sem fim, muitas vezes assinando seu lema "Excelsior!" (que também é o lema do estado de 
                    Nova York). Para manter sua carga de trabalho e cumprir os prazos, ele usou um sistema que era usado 
                    anteriormente por vários estúdios de história em quadrinhos, mas devido ao sucesso obtido, ficou 
                    conhecido como o "Método Marvel". Tipicamente, Lee iria debater uma história com o artista e então 
                    preparar uma breve sinopse ao invés de um roteiro completo. Com base na sinopse, o artista preencheria
                    o número de páginas atribuído determinando e desenhando a narrativa de quadro a quadros. Depois que o 
                    artista desenhava as páginas a lápis, Lee escrevia as palavras balões e legendas e depois 
                    supervisionava as letras e as cores. Com efeito, os artistas foram co-roteiristas, com os primeiros 
                    rascunhos colaborativos que Lee construiu. O artista gravou mensagens para o recém-formado fã-clube 
                    Merry Marvel Marching Society em 1965.
                </p>
                <p>
                    Após a saída de Ditko da Marvel em 1966, John Romita Sr. tornou-se o colaborador de Lee em The Amazing
                    Spider-Man. Em um ano, superou o Fantastic Four para se tornar a revista mais vendida da empresa. As 
                    histórias de Lee e Romita se concentravam tanto na vida social e universitária dos personagens quanto 
                    nas aventuras do Homem-Aranha. As histórias tornaram-se mais atuais, abordando questões como a Guerra
                    do Vietnã, eleições, e movimento estudantil. Robbie Robertson, introduzido em The Amazing Spider-Man 
                    #51 (agosto de 1967) foi um dos primeiros personagens afro-americanos nos quadrinhos a desempenhar um 
                    sério papel coadjuvante. Na série Fantastic Four, Lee e Kirby produziram muitas histórias aclamadas, 
                    bem como personagens que se tornaram centrais para a Marvel, incluindo os Inumanos e o Panteras Negra, 
                    um rei africano que ser o primeiro super-herói negro dos quadrinhos mainstream. A história 
                    frequentemente citada como a melhor realização de Lee e Kirby é a trilogia de três partes 
                    Galacuts Trilogy que começou em Fantastic Four #48 (março de 1966), narrando a chegada de Galactus, 
                    um gigante cósmico que queria devorar o planeta e seu arauto, o Surfista Prateado. Fantastic Four #48 
                    foi escolhida como vigésima quarta na pesquisa 100 Greatest Marvels of All Time pelos leitores da 
                    Marvel em 2001. O editor Robert Greenberger escreveu em sua introdução à história que "Quando o quarto 
                    ano do Quarteto Fantástico chegou ao fim, Stan Lee e Jack Kirby pareciam estar apenas se aquecendo. 
                    Em retrospecto, talvez tenha sido o período mais fértil de qualquer título mensal durante a Era Marvel.
                    " O historiador de quadrinhos Les Daniels observou que" os elementos místicos e metafísicos que assumiu
                    a saga eram perfeitamente adequados ao gosto dos jovens leitores nos anos 1960 ", e Lee logo descobriu 
                    que a história era uma das favoritas nos campi universitários. Lee e o desenhista John Buscema lançaram 
                    a série The Silver Surfer em agosto de 1968.
                </p>
                <p>
                    No ano seguinte, Lee e Gene Colan criaram o Falcão, o primeiro super-herói afro-americano dos 
                    quadrinhos em Captain America #117 (setembro de 1969). Então, em 1971, Lee indiretamente ajudou a 
                    reformar o Comics Code Authority. O U. S. Department of Health, Education and Welfare (Departamento 
                    de Saúde, Educação e Bem-Estar dos Estados Unidos) pediu a Lee para escrever uma história em 
                    quadrinhos sobre os perigos das drogas e o artista concebeu uma subtrama de três edições em The 
                    Amazing Spider-Man #96-98 (maio-julho de 1971), na qual o melhor amigo de Peter Parker, Harry Osborn, 
                    se torna viciado em drogas prescritas. O Comics Code Authority recusou-se a conceder seu selo porque 
                    as histórias mostravam o uso de drogas; o contexto antidrogas foi considerado irrelevante. Com a 
                    cooperação de Goodman e confiante de que o pedido original do governo lhe daria credibilidade, Lee 
                    publicou a história sem o selo. Os quadrinhos tiveram boas vendas e a Marvel ganhou elogios por seus 
                    esforços socialmente conscientes. O Comics Code Authority posteriormente afrouxou o código para 
                    permitir representações negativas de drogas, entre outras novas liberdades.
                </p>
                <p>
                    Lee também apoiou o uso de histórias em quadrinhos para fornecer alguma medida de comentário social 
                    sobre o mundo real, muitas vezes lidando com racismo e intolerância. "Stan's Soapbox", além de 
                    promover um próximo projeto de quadrinhos, também abordou questões de discriminação, intolerância ou 
                    preconceito.
                </p>
                <p>
                    Em 1972, Lee parou de escrever revistas em quadrinhos mensais para assumir o papel de publisher. 
                    Seus últimos roteiros foram publicados em The Amazing Spider-Man #110 (julho de 1972) e Fantastic 
                    Four # 125 (agosto de 1972).
                </p>
                
                <hr>
                <h3>Últimos anos na Marvel</h3>
                <div class="img-artigo">
                    <div>
                        <p>
                            Lee tornou-se o rosto público da Marvel Comics. Fez aparições em convenções de quadrinhos 
                            por todos os Estados Unidos, dando palestras em faculdades e participando de painéis de 
                            discussão. Lee e John Romita Sr. lançaram a tira de jornal do Homem-Aranha em 3 de janeiro de 
                            1977. A colaboração final de Lee com Jack Kirby, The Silver Surfer: The Ultimate Cosmic 
                            Experience, foi publicada em 1978 como parte da série Marvel Fireside Books e é considerada 
                            a primeira graphic novel da Marvel. Lee e John Buscema produziram a primeira edição de The 
                            Savage She-Hulk (fevereiro de 1980), que apresentou a prima do Hulk, e criaram uma história 
                            do Surfista Prateado para a Epic Illustrated # 1 (Primavera de 1980).
                        </p>
                        <p>
                            Lee mudou-se para a Califórnia em 1981 para desenvolver as propriedades de TV e cinema da 
                            Marvel. Foi produtor executivo e fez aparições em adaptações de filmes da Marvel e outros 
                            filmes. Ocasionalmente retornava aos quadrinhos escrevendo com vários projetos de Surfista 
                            Prateado, incluindo uma história de 1982 desenhado por John Byrne, a graphic novel Judgment 
                            Day ilustrada por John Buscema,[78] a minissérie Parable desenhada pelo desenhista francês 
                            Moebius, e a graphic novel The Enslavers com Keith Pollard. Lee foi por um curto 
                            período, presidente de toda a empresa, mas logo se demitiu para tornar-se publisher, 
                            descobrindo que ser presidente era atuar mais sobre números e finanças, e não utilizava sua 
                            capacidade para o processo criativo.
                        </p>
                    </div>
